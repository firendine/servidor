<p color="black"> 
Minnesota Timberwolves (Lobos de Minnesota) es un equipo de baloncesto que juega en la NBA, y tiene su sede en Minneapolis, perteneciente al estado de Minnesota. Entraron en la liga tras la expansión de 1989. Con anterioridad, la ciudad había sido sede de los míticos Minneapolis Lakers, actualmente en Los Ángeles. 

</p>

<h1>Inicios</h1>
<p>
El baloncesto profesional regresó a Minneapolis en 1987, cuando la NBA concedió una de las dos nuevas franquicias (junto con Orlando Magic) a los propietarios originales Marv Wolfenson y Harvey Ratner para comenzar a jugar en la temporada 1989-90. Conocidos como los Timberwolves, el equipo debutó el 3 de noviembre de 1989 perdiendo ante Seattle SuperSonics por 106-94.
El Metrodome fue el primer pabellón del equipo.
</p>

<p>
Cinco días más tarde se estrenarían en su pabellón el Metrodome cayendo con Chicago Bulls por 96-84. Dos noches después, los Timberwolves ganarían el primer partido en su historia, batiendo a Philadelphia 76ers en casa por 125-118 el 10 de noviembre de 1989. Los Wolves, liderados por Tony Campbell con un promedio de 23.2 puntos por partido, finalizó con un balance de 22-60 en el último lugar de la División Medio Oeste. El 17 de abril de 1990 se consiguió el tercer mejor registro de espectadores en un pabellón de la NBA, asistiendo 49.551 al Metrodome para ver perder a su equipo ante Denver Nuggets por 99-88 en el último partido de los Wolves en casa de la temporada.
</p>

<p>
En la siguiente campaña la franquicia se mudó al Target Center, ganando 29 partidos y despidiendo al entrenador Bill Musselman. Con su sucesor Jimmy Rodgers, ex-entrenador de Boston Celtics, el equipo no mejoró, finalizando con el peor registro de la liga, 15-67. Durante los siguientes años, el equipo no supo salir de la mediocridad en la que estaba sumida, e incluso se rechazó por parte de la NBA un movimiento a la ciudad de New Orleans en 1994. Glen Taylor compró el equipo y nombró director general a Kevin McHale.

</p>

    
